                <h4>Day 2, Sep 26 - Theme: Holi</h4>
<p>Holi is a Hindu spring festival celebrated in India and Nepal, also known as the "festival of colours". The festival signifies the victory of good over evil, the arrival of spring, end togetherness. Holi celebrations start on the night before Holi where people gather, perform religious rituals in front of the bonfire, and pray that their internal evil be destroyed in the fire.  The next morning is celebrated as Rangwali Holi– a free-for-all festival of colours, where people smear each other with colours and drench each other. Water guns & water-filled balloons are also used to play & colour each other. It signifies unity.</p>

