<h4>Day 5, Sep 29 - Theme: Lohri</h4>
<p>
    Lohri marks the beginning of the end of winter, coming of spring and the new year. The festival is traditionally associated with the harvest of the rabi crops. The traditional time to harvest sugarcane crops is January, therefore, Lohri is seen by some to be a harvest festival. And thus, Punjabi farmers see the day after Lohri (Maghi) as the financial New Year. It is known as Makar Sankranti in the other part of India.</p>

