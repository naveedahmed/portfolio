            <a name="etymology"></a><h3>Etymology</h3>
            <section>
            <p>Always use the phrase '<span class="text-highlight green">INTERACT 2017 Mumbai</span>'. Note that the term 'INTERACT' must always be in capitals.</p>
            <p>In case of a space constraint, the term 'INTERACT 2017' may be used. 'Mumbai' may be omitted. But this is not recommended.</p>
            <p><span class="text-highlight bold red">Never</span> refer to the event as just <span class="text-highlight red">'INTERACT,' 'Interact,' 'Interct 2017,' or 'Interact 17,'</span> as it might refer to older IFIP INTERACT conferences or other conferences with similar names. This will lead to confusion, brand dilution and lost PR exercises.</p>
            </section>
