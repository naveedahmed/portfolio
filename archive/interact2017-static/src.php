                        <h3>Guidelines</h3>
                        <section>
<p>                           You are eligible to submit an entry to SRC if you currently are an undergraduate or masters level student in any faculty (including design, engineering, architecture, arts, commerce, management etc.), or if you finished your education in the calendar year 2017. 
</p>
<p>A submission could be co-authored by 1-5 people. They need not be from the same educational institution, but all of the 5 people must be eligible to submit to the SRC. Faculty members or professionals who may have contributed as guides or supervisors should be acknowledged separately, and should NOT be listed as "authors" of the submission.
</p>
<p>The reviewing will be done by mentors. The best submissions will be showcased in a session during the conference. 
</p>    
							
                           
                            </section>
                            <h3>Submission Details</h3>
                        <section>
<p>A submission to the SRC should in 2-4 pages describe work in any of the topic areas covered by Human-Computer Interaction. Papers should describe:
</p><ul>
    <li>The research problem and motivation for the research</li>
    <li>Background and related work (within the scope of the paper)</li>
    <li>Research approach</li>
    <li>Novelty of the design/research</li>
    <li>Results</li>
    <li>Contributions to the field, these could be in the form of findings and insights, and or a novel design concept / system, etc.</li>
</ul>  

<p>The paper submission will be judged on the novelty of the idea with respect to current related state-of-art, writing quality and how well the design/research is presented. Paper submissions may be accompanied by videos of any length up to a maximum of 5 minutes and less than 90 MB. The video needs to effectively communicate the research idea. 
</p>
