<h4>Abstract</h4>
<p>Technological advances have made head mounted displays (HMD)s, 3D printing, augmented reality (AR) and virtual reality (VR) more and more common-place. On one hand, VR/AR technologies are transporting us to a different world whereas on the other hand robotic automations and 3D printing is bringing/recreating new worlds for us. As such they have captured the imagination of the Interact Community.  </p><p>Our panel will share their experience working in this space and reflecting on new applications and context of use. We will also consider what the cultural, environmental and social implications are of these technologies. As well as how traditional design methods and process can be applied to these technologies. 
</p>
		

	
