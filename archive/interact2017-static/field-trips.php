                    <h3>Overview</h3>
                    <section>
                        <p>A field trip group typically comprises of 5-8 participants, who are a mix of local and international researchers, practitioners and student volunteers. Each group will have at least 2 local participants who can speak the local languages to facilitate translation. A field trip may last one or two days. It may be organised either on September 25 and/or September 26, 2017.</p>

                        <p>The organising committee will receive field trip proposals which will include:
                        </p>
                        <ol>
                            <li>Social geography of the field site</li>
                            <li>A short profile of the subject of enquiry (E.g. Mobile phone repair workers, health centers, leisure spaces etc.)</li>
                            <li>Methodology (any specific methodological protocol such as video ethnography, contextual enquiry, photo diaries, et al.)</li>
                            <li>Broad research questions</li>
                            <li>Possible/Expected Outcomes</li>
                        </ol>
                        <p>Possible locations of field trips in and near Mumbai include: Dharavi, Behrampada, Bandra, Kotachi Wadi, Mumbai local trains, villages near Palghar, Panvel or Pen etc. (<a href="files/Field_Trip_Photos.pdf" target="_blank">See image references of some of these locations</a>). Possible communities or user groups that a field trip might look at include pregnant women and new mothers, elderly, differently abled, street food vendors, the undertakers of Bandra, migrant communities, construction workers, dabbawallahs, bhajan groups in local trains, tribal communities in the outskirts of Mumbai etc. </p>
                        
<p>A lens or a framework to carry out a field trip may include information such as gender, development, livelihood, employment, entrepreneurship, innovation, child rights, everyday stories, jugaad, hacker / maker culture etc. A method used for a field trip could use shadowing, participant observation, participatory design, usability evaluation, focus group discussion or a combination of these methods.</p>
 
<p>A field trip may result in a rich description of a social phenomenon, a 'day in the life' mapping, an understanding of technology use by a community or a user group, ideas for a new product, testing of an idea, a usability evaluation of a product, development of a new method or mapping a new use case.</p>

<p>Proposals with a relevant scope but needing support in terms of refining some aspects of it, e.g. location, access or local participants, will still be considered. In these cases, the organising committee will assess whether they can be implemented during the conference through a combination of local resources or other proposals.  </p>
                        <p>Once proposals are accepted, groups will be formed according to mutual interests which will be curated/discovered through this process. Before the conference, two online sessions will be organized to facilitate introduction and discussion amongst group members.
                        </p>
                        <p>Each group will be helped with local logistical arrangements for transport, food and user recruitment. Each group will have 15 minutes of time to present their main findings / learnings in a session during the main conference.
                        </p>
