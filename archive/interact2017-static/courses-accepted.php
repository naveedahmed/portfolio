                        <h3>C4: Children Participating in HCI Research</h3>

                        <p><span class="bold">Instructor</span>: Janet Read (University of Central Lancashire, UK)
                        </p>
                        <p>This course will introduce attendees to the challenges and benefits of working with child participants in interaction design and evaluation within the context of HCI. It will outline the most used methods and provide resources to participants so they will be able to carry out effective work with children from 4 to 16 in schools, homes and the outdoors. </p>
                        <p>Delivered by an experienced member of the IFIP WP13.1 SIG in IDC, this tutorial will appeal to researchers and developers working with children and in the design of products for children. The course will especially focus on how cultural biases can get in the way of design and evaluation when children are involved.</p>

                        <h4>Instructor Bio:</h4>
                        <p>The tutor delivering this tutorial is Janet Read, a full Professor of Child Computer Interaction (CCI).
                        </p>
                        <p>She has delivered CCI courses across the globe at venues including Interact, HCI, CHI and IDC and at her home institution she teaches on an MRes course in CCI. </p>

                        <p>With Panos Markopoulos, she co-authored the bestselling book on ‘Evaluating Interactive Technology for and with Children’ and is the current chair of the IFIP TC13 SIG on Interaction Design and Children.
                        </p>
                        <p>She is the Editor in Chief of the International Journal of Child Computer Interaction and an Associate Editor of IJMHCI and IJHCS.
                        </p>
