                        <h3>C2: Design of Location-based Mobile Games: Introduction
</h3>
                        <p><span class="bold">Course Instructors:</span> Christos Sintoris, Nikoleta Yiannoutsou, Nikolaos Avouris (University of Patras, Greece)
                        </p>
                        <p>The objective of this course, is to introduce the participants to location-based games and to the challenges relating to designing them. Key characteristics of this new genre are introduced first, followed by a design framework and a set of design guidelines.
                        </p>
                        <p>Examples of location-based games will be presented and typical design patterns as extracted from previous workshops will be discussed. This course has already been run in the frame of several conferences and summer schools (Sintoris, 2014).
                        </p>
                        <p>Typical course participants include interaction designers, game designers and developers, practitioners and researchers interested in location-based games. The course is presented by researchers who have been involved in many years in designing and studying human interaction with location-based games.
                        </p>
                        <p>Examples of games developed by the course organizers include MuseumScrabble, RebelsVsSpies, Taggling, etc.
                        </p>
                        <h4>Instructor Bio:
</h4>
                        <p>The course organizers and tutors are members of the Interactive Technologies Lab of the University of Patras with over 10 years’ experience in design of location-based games. Examples of games developed include MuseumScrabble, CityScrabble, Taggling, InvisibleCity, etc. They have also published in this area, in particular in the field of learning effect of location-based games.</p>

                        <p>In particular, <span class="bold">Dr Sintoris</span> holds a PhD in design of location based games. He was the main designer of a number of location based indoors and outdoors games. He is particularly interested in aspects related to user interaction and user experience in location-based games.
                        </p>
                        <p><span class="bold">Dr Yiannoutsou</span> holds a PhD in technology enhanced learning. She investigates the emergence of new aspects of learning when using digital technologies. She is interested in location based games and in game design – game modding as learning activity.
                        </p>
                        <p><span class="bold">Prof. Avouris</span> is the Head of the Interactive Technologies Lab HCI Group, professor of HCI for over 25 years. He is particularly interested in location based games in cultural spaces and player participation as a means of shaping cultural experience with technology.
                        </p>
