                        <h3>C3: Designing and Assessing Interactive Systems Using Task Models</h3>

                        <p><span class="bold">Course Instructors:</span> Célia Martinie, Philippe Palanque, Marco Winckler (ICS-IRIT, University Paul Sabatier, France)</p>

                        <p>This two-part course takes a practical approach to introduce the principles, methods and tools in task modelling.
                        </p>
                        <p><span class="bold">Part 1:</span> A non-technical introduction demonstrates that task models support successful design of interactive systems.
                        </p>
                        <p><span class="bold">Part 2:</span> A more technical interactive hands-on exercise of how to "do it right", such as: How to go from task analysis to task models? How to assess (through analysis and simulation) that a task model is correct? How to identify complexity of user tasks …
                        </p>
                        <h4>Instructor Bio:</h4>
                        <p><span class="bold">Philippe Palanque</span> is Professor in Computer Science at University of Toulouse 3. He has been teaching HCI and task engineering classes for 20 years and is head of the Interactive Critical Systems group at the Institut de Recherche en Informatique de Toulouse (IRIT) in France.
                        </p>
                        <p>Since the late 80s he has been working on the development and application of formal description techniques for interactive system. He has worked on research projects to improve interactive Ground Segment Systems at the Centre National d'Études Spatiales (CNES) for more than 10 years and is also involved in the development of software architectures and user interface modeling for interactive cockpits in large civil aircraft (funded by Airbus).
                        </p>
                        <p>He is also involved in the research network HALA! (Higher Automation Levels in Aviation) funded by SESAR program which targets at building the future European air traffic management system.
                        </p>
                        <p>The main driver of Philippe’s research over the last 20 years has been to address in an even way Usability, Safety and Dependability in order to build trustable safety critical interactive systems.
                        </p>
                        <p>As for conferences he is a member of the program committee of conferences in these domains such as SAFECOMP 2013 (32nd conference on Computer Safety, Reliability and Security), DSN 2014 (44th conference on Dependable Systems and Networks), EICS 2014 (21st annual conference on Engineering Interactive Computing Systems) and was co-chair of CHI 2014 (32nd conference on Human Factors in Computing Systems) and research papers co-chair of INTERACT 2015.
                        </p>
                        <p><span class="bold">Célia Martinie</span> is Assistant Professor in Computer Science at University of Toulouse 3. She has been working on task modeling techniques for the design and development of interactive systems since the beginning of her PhD in 2009.
                        </p>
                        <p>Prior to that, she worked in the mobile industry (Motorola) during 8 years, and has contributed to the design and development of user interfaces for mobile devices. She is the principal investigator of the projects related to the design and development of the HAMSTERS notation and tools.
                        </p>
                        <p>She applied the task modeling approaches to a variety of systems including satellite ground segments, interactive cockpits of large civil aircrafts and air traffic control workstations.
                        </p>
                        <p><span class="bold">Marco Winckler</span> is associated professor in Computer Sciences at Université Paul Sabatier, Toulouse, France. He is member of the Interactive Critical System (ICS) research team where he investigates models, methods, techniques and tools to support the development of reliable, usable and effective interactive systems.
                        </p>
                        <p>He obtained a PhD degree in Informatics (2004) from Université of Toulouse 1 Capitole (Toulouse, France), a Master's degree in Computer Science (1999) from the Universidade Federal do Rio Grande do Sul (Porto Alegre, Brazil) and a Post-doc degree from the Université catholique de Louvain (Louvain-la-Neuve, Belgium).
                        </p>
                        <p>His research combines topics of Engineering Interactive Systems, Human-Computer Interaction and Web Engineering. He also serves as chair for the IFIP working group 13.2 on Methodologies for User-Centered Systems Design and secretary of the IFIP TC 13 on Human-Computer Interaction. Further information on the web site: http://www.irit.fr/~Marco.Winckler
                        </p>

