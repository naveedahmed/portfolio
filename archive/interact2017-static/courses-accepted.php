                        <h3>C6: Introduction to Service Design for Digital Health
</h3>
                        <p>Instructors: Ashley Colley, Henna Marttila (University of Lapland, Finland)
                        </p>
                        <p>This course addresses service design of wellness and health services which include digital components. The course will cover methodological and practical aspects of service design, focusing on three key methods – stakeholder maps, consumer journey and blueprint.
                        </p>
                        <p>The service design methods are tried out with interactive exercises, where participants in groups apply the methods to a digital health design case. Service design is an emerging field, which applies a holistic design approach to understand and design for human experience.
                        </p>
                        <p>With an increasing number of digital wellness and health services, both in commercial and public frontiers, it is important to develop services that are easy to use, and where the consumer’s journey through the service pathways are fluent and consistent.
                        </p>
                        <h4>Instructor Bio:
</h4>
                        <p><span class="bold">Ashley Colley (M)</span> is a researcher at the University of Lapland, completing his doctor of arts in summer 2017. Colley has more than 25 years’ professional industry experience, and has co-authored more than 50 peer-reviewed research papers. </p>

                        <p>Additionally, Colley is an active entrepreneur, being a co-founder of OuraHealth Ltd. (www.ouraring.com) and a board member at QuietOn Ltd. (www.quieton.com). He is a multi-skilled creative technologist with hands-on capabilities on user centric design methods, interaction design, graphic design and software development.
                        </p>
                        <p><span class="bold">Henna Marttila</span> MA is working as a research trainee specializing to service design at University of Lapland, working now in Critical Communication, Safety and Human-centered Services of the Future (CRICS) project.
                        </p>
                        <p>One of her research focus has been to improve people's wellbeing and safety. She sees communication one of the main things in designing these two issue. She is developed her skills in service design but also in the field of graphic design and game design.
                        </p>
