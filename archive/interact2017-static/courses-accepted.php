                        <h3>C5: Designing for Accessibility</h3>

                        <p><span class="bold">Instructors:</span> Helen Petrie (University of York, UK), Gerhard Weber (Technische Universität Dresden, Germany), Jenny Darzentas (University of York, UK)
                        </p>
                        <p>Involving a wide variety of end users and in particular those with a disability or who are older when designing web pages and apps requires a good understanding of how to involve those end users, assistive technology, and evaluation methods.
                        </p>
                        <p>This course will introduce the basics of assistive technologies built into using mobile phones and describe major barriers in web pages, and how to analyze them with end users. It will also outline a range of appropriate tools to use in this work.
                        </p>
                        <h4>Instructor Bio:
</h4>
                        <p><span class="bold">Professor Helen Petrie</span> holds the Chair in HCI at the University of York, current chair of IFIP WG 13.3. Her research focuses on the design and evaluation of technology for disabled and older people. She has been teaching web accessibility and evaluation methods for more than two decades and has given tutorials for example at the WWW conference.</p>

                        <p><span class="bold">Professor Gerhard Weber</span> holds the Chair in HCI at Technische Universität Dresden, past chair of IFIP WG 13.3. His research focuses on personalization of multimodal systems for the benefit of people with a disability.
                        </p>
                        <p>He has contributed to the development of a Massive Open Online Course (MOOC) on Digital Accessibility on the FutureLearn platform, attracting more than 3000 participants throughout the current two runs since October 2016.</p>
                        <p>He teaches about accessibility of documents including web pages and their evaluation using automatic and manual approaches, navigation and mobility of blind people, as well as tactile graphics and haptic usability.
                        </p>
                        <p><span class="bold">Jenny Darzentas</span> is a Marie Curie Experienced Research Fellow in the Department of Computer Science at the University of York. Her research interests are in the areas of Design for All and e-Accessibility, in particular Service Design and Information (content) design. These activities are applied in online learning, decision support systems and computer mediated communication.
                        </p>
