                        <h3 class="no-top-margin">C1: The Science behind User Experience Design</h3>

                        <p><span class="bold">Course Instructor</span>: Asad Ali Junaid (India)
                        </p>
                        <p>Planning and conducting User Experience (UX) activities in a structured and scientific manner has many advantages. It is important that UX Professionals understand the scientific basis of UX methods and leverage them to enhance the UX of the application being designed.</p>

                        <p>It would also be easier for the UX designer to get a buy-in from the stakeholders if his design recommendations are based in scientific logic and whetted by supporting data.
                        </p>
                        <p>In this course, UX relevant social sciences based scientific concepts and methods will be presented to the audience in a way which is simple to understand and easily to assimilate.
                        </p>
                        <h4>Instructor Bio:
</h4>
                        
<p>						Asad Ali Junaid is currently with a US based MNC as a Sr. User Experience Designer designing their retail analytics and enterprise business products.
</p>
<p>     Junaid has more than ten years of industry experience in User Experience and Interaction Design across organizations such as ABB Corporate Research, IBM, Intel and Human Factors International.
</p>
<p>     Junaid has a MS Degree in Human Factors Psychology and is a Certified Usability Analyst (CUA). Junaid also has a MS Degree in Electrical Engineering as well and brings a multi-disciplinary approach to problem solving.
</p>
<p>     Junaid loves to teach and believes that the best way to learn something is to teach it. He regularly conducts a one credit course in HCI &amp; UX for the IT Department of PSG Tech, Coimbatore. Junaid has taught UX Design & HCI at the organizations he has previously worked at as well.
</p>
<p>      In order for the organization to compete most effectively in the marketplace, Creativity must be a vital skill of its employees. Junaid has also conducted numerous workshops on Creative Thinking.
</p>
<p>Junaid is the Co-Founder of StyleMyFit Pvt. Ltd. which makes Made-to-Measure Apparels to complement body type & personal styles – with the convenience of e-commerce. Junaid is also the author of an epistolary novel – ‘And We Remained’ – told through emails and first person accounts of events.
</p>                       
                       
                       
