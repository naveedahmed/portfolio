			<a name="making-pres"></a><h3>Guidelines for Making the Presentation</h3>
			<h4>Text</h4><br>
			<ol>
				<li>Use minimal text on the slide.</li>
				<li>Use sufficient contrast for text and background colors.</li>
				<li>Use a larger font size (18pt or larger)</li>
				<li>Use standard system fonts so that presentation in accessible from any device or medium.</li>
				<li>Use sans serif fonts</li>
				<li>Use sufficient white space to avoid clutter.</li>
				<li>Avoid capitalization.</li>
				<li>Avoid too many bold or fancy fonts.</li>
				<li>Avoid the use of italic font style.</li>
				<li>Try not to use more than one font type per slide.</li>
			</ol>
			<h4>Visuals</h4><br>
			<ol>
				<li>Minimize the number of visuals on slides. Use only visuals which will provide value addition.</li>
				<li>Make visuals as simple as possible.</li>
				<li>Provide description to the graphs and charts. Summarize if applicable.</li>
				<li>Maintain consistency in the contrast and brightness as used for the text.</li>
			</ol>
			<h4>Videos</h4><br>
			<ol>
				<li>Provide means to control the speed of animations so they can be explained elaborately.</li>
				<li>Provide captions to the videos and describe the audio.</li>
				<li>Provide audio track with video descriptions, for blind or visually impaired audience.</li>
				<li>For videos with dialogue, include closed captions or subtitles in a supported format for deaf or hard of hearing audience.</li>
			</ol>
			<h4>General</h4><br>
			<ol>
				<li>Minimize the number of slides.</li>
				<li>Ensure that color is not the only means of conveying information.</li>
				<li>Give every slide a unique title.</li>
			</ol>
			<h4>In case presentation is to be shared with audience as a soft copy:</h4><br>
			<ol>
				<li>Provide alt text to images, graphics, shapes, charts, tables. Screen reader software reads the alternative text so visually challenged readers know what’s in the image.</li>
				<li>Ensure that content in the slides can be read by screen reader in the intended order.</li>
				<li>Use consistent templates in the presentation. Preferably use built-in templates. Some tools like Microsoft PowerPoint Presentation have inherent feature in the templates that ensure reading order works for everyone.</li>
				<li>Use hyperlinks instead of using whole link. Provide meaningful hyperlink text.</li>
			</ol>
