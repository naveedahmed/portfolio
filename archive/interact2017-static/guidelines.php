<a name="giving-pres"></a><h3>Guidelines for Giving the Presentation
    </h3><h4>Text</h4><br>
<ol>
    <li>Ensure that all the text mentioned in the slide is covered during presentation, without reading the slide exactly as it is. Convey the information for all the points displayed.</li>
    <li>Use simple language which can be understood across all cultures.</li>
    <li>Avoid using of slang to describe, unless required contextually.</li>
</ol>
<h4>Visuals
    </h4><br><ol>
        <li>Describe each image so that visually challenged audience with understand well.</li>
    </ol>
<h4>Videos</h4><br>
<ol>
    <li>Give a brief description about the video before it is played so that context is set for visually challenged audience.</li>
    <li>Appropriately control the speed of video or animations so that they can be described fully.</li>
</ol>
<h4>General</h4><br>
<ol>
    <li>Instead of just pointing to a region in the slide, mention the region of interest explicitly to avoid ambiguity.</li>
    <li>Clearly describe any images, charts, tables, etc. that are in the presentation.</li>
    <li>Explain each slide in an expressive manner.</li>
    <li>Speak clearly, face forward and avoid covering your mouth.</li>
    <li>Avoid side conversations.</li>
    <li>Avoid acronyms and colloquialisms.</li>
    <li>Identify yourself each time you speak (if in a group).</li>
    <li>Describe visual description of non-verbal action in the meeting e.g. “There are several audience members who have raised their hands.”</li>
    <li>When addressing someone specifically, say his/her name to confirm you have that person’s attention.</li>
    <li>Indicate when you are finished speaking.</li>
    <li>If there is an interpreter, look at/address the participant, not the interpreter.</li>
</ol>


