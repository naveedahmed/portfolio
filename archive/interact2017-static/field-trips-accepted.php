					<h4>FT5: Modelling Less-literate User’s Choices of Smartphone Authentication Modes</h4>
					<div class="ft-proposer-info">Pankaj Doke, Sylvan Lobo, Shyama V. S, Ulemba Hirom, Mridul Basumotari // shyamav.s[at]tcs.com</div>
					<div class="ft-abstract">
						<h5>Abstract</h5>
						<p>Smartphones are increasingly becoming a device of choice and are imperative in the discourse of Digitization of services such as banking within a developing country like India. At the same time, a large population within India is less-literate and can face unique security related challenges with smartphones. We believe that Emergent Users are the next set of users who are likely to adopt the smartphone and technology in larger context. Amongst these emergent users we expect that a large class of user will be less-literate, more comfortable with native languages and have never directly consumed any digital technology based information system. </p>
						<p>
							Within the eco-system of the smartphone, namely, the phone itself comprising of an operating system and mobile applications on the phone as well as those on the Internet cloud, a mandatory creation of a Digital Identity in the form of a Google Account is required. Currently, the notion of digital identity Authorization in most smartphone-based applications is implemented using a variety of choices, such as passwords, PINs, patterns, biometrics such as fingerprint, voice, etc. In the context of our users, the emergent users, each of these authentication modes has a usability aspect to it, which has a strong influence on the user and their adoption. For example, issues such as literacy levels are expected to play a role in the composition of passwords or use of local languages in usability of passwords. </p>
						<p>
							In this Field Study, we wish to explore the Migration Model of the users amongst all these authentication modes. For example, how do users trade off PIN to Passwords to Biometrics; what triggers in their context of use, bring about these migrations when potentially the user may have chosen an alternative authentication mode. </p>
						<p>
							For the field trip study, the participants would meet equal number of both male and female users. They would be given a paper prototype and follow three scenarios- 1) showing the multiple mode of authentication, 2) choosing of one authentication with given context, and 3) choosing of password option from the chosen authentication mode within the context. All the finding in the form of artefacts, field notes, videos and photos would be analysed with the participants, and reported as a part of migration model.

						</p>
