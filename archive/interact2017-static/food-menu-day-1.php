<h4>Day 1, Sep 25 - Theme: Diwali</h4>
<p>Diwali is the five-day festival of lights, celebrated by millions of Hindus, Sikhs and Jains across the world. The festival, which coincides with the Hindu New Year,celebrates new beginnings and the triumph of good over evil and light over darkness. On Diwali night, people dress up in new clothes, light up diyas (lamps and candles) inside and outside their home to help Lakshmi, the goddess of wealth, fertility and prosperity, to find her way into people's homes.</p>
