<h3>Human-Computer Interaction – the Life and Times, and the History</h3>

<p>HCI and Interact are here to stay. Many of us are born in a society where social media and smartphones are the common way of living. We take the opportunities for granted, and just develop ideas to broaden the use and to enable ever more innovative application, including cheap facilities for developing regions.
</p>
<p>We label our field: Human-Computer Interaction, Interaction Design, Usability Engineering – old words that could now be replaced by “Experience Design”. Or should they? We live in the clouds, our friends and family live on Facebook, and our watch is our coach and our physician. But how did this all start, and who were the pioneers to found the science and practice of Affordance Design, Cognitive Engineering and Software Ergonomics? Is it industry that started by inventing interactive systems, or the scientists who build theories and models for the user interface: What were the chickens that produced the egg of HCI? 
</p>
<p>In this talk you will learn about blue-grass movements in different parts of the world that did result in understanding, theories, prototypes, and approaches. The talk will illustrate how this was discussed and disseminated in communities and at conferences, how this evolved into education and standards, and how this resulted in recognition of our professional field as the base for design of usable, comprehensive, enjoyable, absorbing and un-evitable tools, culminating in a new context of life and society, and new cultures.
</p>
<h4>Speaker Bio</h4>
<p>Gerrit C. van der Veer has been a researcher and teacher in University since 1961. He started in Cognitive Psychology, moved to Cognitive Ergonomics, and into Computer Science, where he specialized in design of interactive systems. He has been developing HCI curricula, and teaching HCI in many European countries including Belgium, Germany, Spain, Italy, Romania, and the Netherlands, as well as in China. 
</p>
<p>His research concerns user centered design methods, task modeling, individual differences and cultural diversity, mental models, cultural heritage, and visualization. 
</p><p>He is currently supervising PhD Students for several Dutch Universities and in Italy; and teaching courses at the Maritime University of Dalian (China) Sino-European Usability Centre; and at LuXun Academy of Fine Arts, Department of Multimedia and Animation, Shenyang (China).</p>

<p>Gerrit is Past President of ACM SIGCHI, the world leading international society for human-computer interaction, and Co-founder and Past President of the European Association of Cognitive ergonomics.
</p></div>
