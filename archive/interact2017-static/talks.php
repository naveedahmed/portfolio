				<h3>Weaving new Mobile UX futures through integrated innovation across 3 Continents, 4 Countries and a diversity of perspectives</h3>
			<div class="info yellow">Andy Cockburn was originally scheduled to give the closing keynote at INTERACT 2017. Unfortunately, Andy has had an accident - he injured his neck while trying to teach his kids how to backflip on a trampoline! He cannot fly to India (he's OK and we wish him a speedy recovery). Our closing keynote will now be given by Matt Jones - as you'll see from his abstract, Matt is often travelling the world and is a regular visitor to Mumbai. We're delighted that he is able to join us for the conference. (<a href="#" data-toggle="modal" data-target="#andy-details">Read about Andy's now-cancelled talk</a>)</div>
<p>Mobile and ubiquitous computing researchers have long imagined future worlds for users in developed regions. Steered by such visions, they have innovated devices and services exploring the value of these with and for individuals, groups and communities. Meanwhile, such radical and long-term explorations are uncommon for what have been termed emergent users; users, that is, for whom advanced technologies are just within grasp. Instead, a driving assumption is, perhaps, that today’s high-end mobile technologies will "trickle down" to these user groups in due course. 
</p>
<p>For the past three years, our team of researchers and partner organisations have started to explore how to do future envisioning that includes emergent communities. We have carried out intensive, coordinated innovation-prototyping-deploym ent yearly cycles that has seen us work with people from townships in South Africa and informal and slum districts in India and Kenya.  These activities have seen the development of a number of novel mobile devices and services that would not have emerged without the insights provided and integrated across these regions.
</p>
<p>In this talk,  I will explore what mobile technologies might be like if emergent users are directly involved in creating their visions for the future 5–10 years from now; explain and reflect on our methods, highlighting the success and failures; detail some of the platforms and devices we've created; and, argue that such innovation is also vital to re-invigorate mobile design for the "traditional users" (like me) in the rest of the world. 
</p>		


