				<h3>Faster, More Learnable, and Preferred: Experiences Using Applied Psychology to Engineer Interactions</h3>
			
<p>				Research and practice in Human-Computer Interaction has come a long way in the thirty-four years since Card, Moran and Newell published their seminal book 'The Psychology of Human-Computer Interaction'. However, the central tenet of their book remains every bit as relevant today as it was in 1983 -- the design of user interfaces can and should be informed by engineering methods that are based on understanding from applied psychology.
</p>
<p>This talk will review several research projects that I have conducted with my students and colleagues, which aim to improve interaction by applying human factors lessons from psychology. The review is structured around four broad categories of human factors: motor coordination, spatial memory, skill acquisition, and biases in subjective experience. 
</p>
<p>Within these categories I will describe interfaces and experiments that demonstrate how interaction can be positively (and negatively) influenced by the factors, across interactive tasks including scrolling, command invocation, text messaging, file navigation, and the experience of assistive interfaces.
</p>
<p>A key objective of the talk is to demonstrate, through examples, that lessons from applied psychology can (and should) be used to improve performance and experience with interactive systems.
</p>
		<h4>Speaker Bio</h4>
<p>		Andy Cockburn is a Professor in the Department of Computer Science and Software Engineering at the University of Canterbury in Christchurch, New Zealand, where he directs the Human Computer Interaction Lab.
</p>
<p>Andy's research focuses on designing, evaluating and modelling user interfaces, with a focus on understanding and exploiting specific underlying human factors. His contributions include many interface designs that use human spatial memory to support expertise development in basic tasks such as file retrieval, command invocation, window switching, and scrolling.
</p>
<p>Dr. Cockburn serves on the Editorial Boards of ACM ToCHI, the Human-Computer Interaction Journal, and Foundations and Trends in Human-Computer Interaction. He was papers co-chair for CHI 2014 and 2015. In 2015 he was inducted to the CHI Academy.
</p>
