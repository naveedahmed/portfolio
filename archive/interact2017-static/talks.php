				<h3>Understanding Consumer India and
what businesses need to do to serve them better
</h3>
<p>				People who address Consumer India or People India and develop products for them need to get away from traditional / offer wrong mental models of what emerging market consumers are like or what poor people and uneducated people are like – what their capabilities are, what their priorities are, what their mindsets and life conditions are and so on.
</p>
<p>India is a never-before world.  Never-Before in human history have we had so many young people with modest incomes and even more modest education, come of age post the birth of the internet and the mobile and digital money, combined with pathetic infrastructure and informal service sector jobs.  
</p>
<p>The logic of this world of Consumer India is to be listened to, from the inside , in order to serve it better, and build capabilities and products and ideas that can be valuable in other markets too.
</p>
<p>Sensible as all this seems, why do companies have trouble doing this?  Even champion global companies?
</p>
