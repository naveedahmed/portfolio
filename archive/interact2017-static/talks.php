				<h3>Amplifying Human Abilities: Digital Technologies to Enhance Perception and Cognition</h3>

<p>		Historically, the use and development of tools is strongly linked to human evolution and intelligence. The last 10.000 years show a stunning progress in physical tools that have transformed what people can do and how people live. Currently, we are at the beginning of an even more fundamental transformation: the use of digital tools to amplify the mind. 
</p>		
<p>		Digital technologies provide us with entirely new opportunities to enhance the perceptual and cognitive abilities of humans. Many ideas, ranging from mobile access to search engines, to wearable devices for lifelogging and augmented realty application give as first indications of this transition. 
</p>		
<p>		In our research we create novel digital technologies that systematically explore how to enhance human cognition and perception. Our experimental approach is to: first, understand the users in their context as well as the potential for enhancement. Second, we create innovative interventions that provide functionality that amplifies human capabilities. And third, we empirically evaluate and quantify the enhancement that is gained by these developments. 
</p>		
<p>		It is exciting to see how ultimately these new ubiquitous computing technologies have the potential for overcoming fundamental limitations in human perception and cognition. 
</p>		
		
			<h4>Speaker Bio</h4>
<p>Albrecht Schmidt is a professor for Human Computer Interaction and Cognitive Systems at the University of Stuttgart. Previously he was a Professor for User Interface Engineering and Pervasive Computing at University of Duisburg-Essen. 
</p>			
<p>			In 2006/2007 he had a joined appointment between the University of Bonn and the Fraunhofer Institute for Intelligent Analysis and Information Systems (IAIS) working in the area of Media Informatics. He studied computer science in Ulm, Germany and Manchester, UK and receive in 2003 a PhD from the Lancaster University in the UK. 
</p>			
<p>			For the last 15 years Albrecht has been dedicated to creating usable systems. The focus of his current work is on novel user interfaces to enhance and amplify human cognition. He is working on interaction techniques and interactive applications in the context of mobile and ubiquitous computing, including new communication interfaces and user interfaces in the car. 
</p>			
<p>			Albrecht has published well over 200 refereed archival publications and his work is widely cited. He is co-founder of the ACM conference on Tangible and Embedded Interaction (TEI) and initiated the ACM conference on Automotive User Interfaces (auto-ui.org). 
</p>			
<p>			In 2014 Albrecht co-chaired the ACM SIGCHI program. He is on the editorial board of ACM ToCHI, edits a forum on interaction technologies in the ACM Interactions magazine, and has a column in the IEEE Pervasive Computing Magazine. 
</p>			
<p>			In 2016 Albrecht Schmidt received a ERC Consolidator Grant to work on the Project “AMPLIFY: Amplifying Human Perception Through Interactive Digital Technologies”.
</p>				
