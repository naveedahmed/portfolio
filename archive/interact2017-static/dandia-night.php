<h3>What is Dandia?</h3>
<p><a href="https://en.wikipedia.org/wiki/Dandiya_Raas" target="_blank">Dandia Raas</a> is a traditional dance festival of Gujarat and Rajasthan and is celebrated in many parts of western India with great vigour during the festival of Navratri. People come together to celebrate this vibrant festival by dressing up in the traditional and colourful outfits and dance in praise and prayer of Goddess Durga. The festival goes on for nine nights (hence the name, nav meaning nine, and ratri meaning nights) and the dance form has many variations across India. Dandia in particular is played with sticks that represent the swords of Goddess Durga, and Garba, another dance form that is practiced during this festival is played with the clapping of the hands while dancing. The festival is very popular, especially among the youth.
</p>
