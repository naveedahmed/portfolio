					<h4>WS6: Beyond Computers: Wearables, Humans, And Things - WHAT!</h4>
					<h6>By <a href="http://ifip-tc13.org/working-groups/" target="_blank">IFIP TC13 WG 13.7</a></h6>
					<div class="ft-proposer-info">Peter Dannenmann // peter.dannenmann[at]hs-rm.de</div>
