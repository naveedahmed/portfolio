						<h5>Abstract</h5>
						<p>User activities and behaviours are “scripted” by the products they use. Therefore, designers intentionally or unintentionally end up shaping the user behaviour. </p>
						<p>Service design, which factors in longer span of user engagement has great potential to influ-ence user behaviour through the appropriately designed product-service systems. There is a need to influence and change user behaviours in their own interest to meet several social challenges, be it at the level of an individual (e.g. health and well-being) or society (e.g. global warming).
