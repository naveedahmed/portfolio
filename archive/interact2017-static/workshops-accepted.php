					<div class="ft-abstract">
						<h5>Abstract</h5>
						<p>Considerable attention has been paid for years to the relationships between humans and computers. But, over the years, the computer chip migrated from the computer internal organs to many other devices - to things, wearables, and even onto the skin (skinnables) and into the human body (implantables).
						</p>
						<p>This workshop will focus on how this revolution may affect the way we look at the relationships between humans and among humans, human elements and computing devices and what should be done to improve these interactions and ‘entanglements’ and to understand them better.
						</p>
