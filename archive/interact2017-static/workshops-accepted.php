						<h5>Abstract</h5>
 
<p>Whilst usability has been the most prominent user interface property in early HCI research other properties such as accessibility, inclusive design, user experience and, more recently security, trust and resilience might also affect the development of interactive system. 
</p>
<p>A good example is security which, by recommending users to deal with passwords reduces system usability by placing a burden on users. 
</p> 
<p>This workshop is open to everyone who is interested in multiple user interface properties while building their systems and how different these are valued by different stakeholders. We expect a high participation of the members of IFIP WG 13.2 and IFIP WG 13.5. 
</p>
<p>We invite participants to present position papers describing real-life case studies that illustrate the tradeoffs between two or more user interface properties. Any property related to user interface design is welcome but two or more properties should be addressed in the same contribution. We are also interested in methods, theories and tools for managing multiple user interface properties. Position papers will be published in adjunct conference proceedings of INTERACT 2017. 
