<h4>Day 4, Sep 28 - Theme: Ganesh Chaturthi</h4>
<p>
    It is the Hindu festival that reveres god Ganesha. A ten-day festival, it starts on the fourth day of Hindu luni-solar calendar month-Bhadrapada, which typically falls in Gregorian months of August or September. The festival is marked with installation of Ganesha clay idols privately in homes, or publicly on elaborate pandals (temporary stage). The festival ends on the tenth day after start, wherein the idol is carried in a public procession with music and group chanting, then immersed in nearby water body such as a river or ocean, thereafter the clay idol dissolves and Ganesha is believed to return to Mount Kailash to Parvati and Shiva</p>

